export class CharacterData {
  constructor(data) {
    this.towerIndex = data.towerIndex;
    this.characterIndex = data.characterIndex;
    this.team = data.team;
    this.name = data.name;
    this.hp = data.hp;
    this.speed = data.speed;
    this.artifact = data.artifact;
    this.notes = data.notes;
  }
}
